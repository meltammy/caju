import { formatCpf } from "~/utils/formatters/formatCpf";

describe("formatCpf", () => {
  it.each([
    ["12345678909", "123.456.789-09"],
    ["123.456.789-09", "123.456.789-09"],
    ["123456789", "123.456.789"],
    ["123", "123"],
    ["", ""],
    ["00000000000", "000.000.000-00"],
    ["11111111111", "111.111.111-11"],
    ["22222222222", "222.222.222-22"],
    ["333.333.333-33", "333.333.333-33"],
    ["!@#$%12345678900", "123.456.789-00"],
  ])("should format CPF input %s to %s", (input, expected) => {
    expect(formatCpf(input)).toBe(expected);
  });
});
